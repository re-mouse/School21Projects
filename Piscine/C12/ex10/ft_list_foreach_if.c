#include "ft_list.h"

void	ft_list_foreach_if(t_list *b, void (*f)(void *), void *d, int (*c)())
{
	while (b != 0)
	{
		if ((*c)(b->data, d) == 0)
			(*f)(b->data);
		b = b->next;
	}
}
